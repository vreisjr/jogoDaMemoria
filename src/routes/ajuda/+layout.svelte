	<h2>Introdução – Jogo da Memória e seus benefícios para a saúde mental</h2>

	<p>
		O jogo da Memória é uma atividade lúdica e educativa que consiste em encontrar pares de cartas
		iguais, utilizando principalmente a atenção, o foco e a capacidade de recordação. Apesar de sua
		simplicidade, ele é amplamente reconhecido como uma ferramenta eficaz para estimular funções
		cognitivas importantes, como memória visual, concentração, raciocínio rápido e agilidade mental.
	</p>

	<p>
		Essa prática contribui significativamente para a saúde mental, ajudando a prevenir o estresse,
		melhorando a capacidade de resolução de problemas e até atuando como exercício preventivo para
		doenças relacionadas à perda de memória. Além disso, por ser um jogo interativo e divertido,
		também auxilia no desenvolvimento socioemocional, promovendo a convivência, o trabalho em equipe
		e momentos de descontração.
	</p>

	<h2>Como será o jogo?</h2>

	<p>
		O jogo será composto por cartas viradas para baixo. Cada participante, em sua vez, deverá virar
		duas cartas e tentar encontrar o par correspondente. Caso acerte, continua jogando; se errar,
		passa para o próximo jogador.
	</p>

	<p>
		Vence quem encontrar mais pares ao final. O diferencial deste jogo está nos temas
		personalizados, tornando a experiência mais divertida e próxima da realidade dos participantes.
	</p>

	<h2>Temas disponíveis</h2>
	<ol>
		<li>Animes – Com imagens de personagens conhecidos</li>
		<li>
			“Os patos” – Serão fotos de pessoas da turma de IPI que perderão no jogo “nem à pato” (de
			forma divertida e descontraída)
		</li>
		<li>Frutas – Imagens simples e ideais para níveis iniciais</li>
		<li>Aleatório – Objetos variados para aumentar a dificuldade</li>
	</ol>
	<h2>Níveis de dificuldade</h2>
	<p>Nível e Características</p>

	<ul>
		<li>Básico: Poucas cartas (ex: 6 pares) com imagens simples e repetitivas, como frutas</li>
		<li>
			Intermediário: Número médio de cartas (ex: 12 pares), tema animes ou objetos aleatórios
			simples
		</li>
		<li>
			Avançado: Muitas cartas (ex: 20 ou mais pares), podendo incluir imagens parecidas entre si e o
			tema “os patos” com fotos reais para provocar confusão e tornar mais desafiador.
		</li>
	</ul>

	<p>
		Cada nível foi pensado para desenvolver progressivamente a capacidade cognitiva dos jogadores,
		começando com associações simples até desafios mais complexos que exigem atenção e memória de
		longo prazo.
	</p>

	<h2>Conclusão</h2>

	<p>
		O jogo da Memória, além de proporcionar diversão e integração entre os participantes, é um
		excelente estímulo para o cérebro, atuando de forma positiva na saúde mental. Através dos
		diferentes níveis e temas criativos, os jogadores poderão desenvolver habilidades cognitivas de
		forma gradual e descontraída, tornando a atividade não apenas educativa, mas também marcante e
		divertida para toda a turma...
	</p>
</div>

